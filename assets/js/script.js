// Language Toggle Functionality
let currentLang = 'zh'; // Default to Chinese

function toggleLanguage() {
    currentLang = currentLang === 'zh' ? 'en' : 'zh';
    updateLanguage();
    updateLangToggleButton();
}

function updateLanguage() {
    // Update all elements with data attributes
    document.querySelectorAll('[data-zh][data-en]').forEach(element => {
        const text = currentLang === 'zh' ? element.dataset.zh : element.dataset.en;
        element.textContent = text;
    });
    
    // Update page title
    document.title = currentLang === 'zh' ? 'FudanEGA - 电子创客社团' : 'FudanEGA - Electronic Geek Association';
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLang === 'zh' ? 'zh-CN' : 'en';
}

function updateLangToggleButton() {
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        langToggle.textContent = currentLang === 'zh' ? 'EN' : '中文';
    }
}

// Initialize language toggle
document.addEventListener('DOMContentLoaded', () => {
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        langToggle.addEventListener('click', (e) => {
            e.preventDefault();
            toggleLanguage();
        });
    }
    
    // Initialize with Chinese
    updateLanguage();
    updateLangToggleButton();
});
